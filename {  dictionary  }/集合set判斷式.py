a = {1, 2, 3, 4}
b = {3, 4, 5, 6}

#---- & ----交集
print(a & b)  #列印重疊的元素
#---- | ----聯集
print(a | b)  #列印a,b全部元素,重疊只列印一次
#----「-」----差集
print(a - b)  #計算在只有a才有的元素
print(b - a)  #計算在只有b才有的元素
#---- ^ ----對稱差集
print(a ^ b)  #只印出重疊的元素
#---- == ----等於
print(a == b)  #計算兩個set是否相等,相同True,不同False
#---- != ----不等於
print(a != b)  #與等於結果相反
#---- in ----是成員in
print(a in b)  #可以測試指定元素是否是集合中的元素
#----not in ----
print(2 not in b)
